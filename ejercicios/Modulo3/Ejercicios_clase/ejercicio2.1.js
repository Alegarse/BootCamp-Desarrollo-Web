console.log("Ejercicio 1. Determinar si un número es positivo, negativo o cero:")
const number = -34
if (number < 0) {
    console.log(`${number} es un número negativo`)
} else if (number === 0) {
    console.log(`${number} es igual a cero`)
} else {
    console.log(`${number} es un número positivo`)
}